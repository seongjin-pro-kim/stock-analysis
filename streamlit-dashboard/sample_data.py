def get_sample_equity_curve() -> pd.DataFrame:
    """계좌별 30일 자산 추이 데이터"""
    dates = pd.date_range("2026-02-16", periods=30, freq="B")
    np.random.seed(42)

    accounts = {
        "1234": {"start": 20000000, "drift": 0.003, "vol": 0.012},
        "5678": {"start": 15000000, "drift": 0.002, "vol": 0.010},
        "9012": {"start": 8000000,  "drift": 0.001, "vol": 0.015},
        "3456": {"start": 5000000,  "drift": 0.002, "vol": 0.008},
    }

    rows = []
    for acc_id, params in accounts.items():
        val = params["start"]
        for d in dates:
            rows.append({"date": d, "account_id": acc_id, "value": round(val)})
            val *= (1 + np.random.normal(params["drift"], params["vol"]))
    return pd.DataFrame(rows)


